'use strict';
const assert=require('assert');
const Core=require('../core.js');
function base(){return {obligations:[],incidents:[],checks:[],whsHazards:[],insurance:[],supervision:[]};}
assert.strictEqual(Core.highest(['green','amber','red']),'red');
assert.strictEqual(Core.dueState(new Date(Date.now()-1000).toISOString()),'overdue');
assert.strictEqual(Core.dueState(new Date(Date.now()+30*60000).toISOString()),'due_soon');
const ob={requiredEvidence:['acceptance','outcome_record'],evidence:[{type:'acceptance'}],outcome:''};
assert.deepStrictEqual(Core.missingEvidence(ob),['outcome_record']);
assert.strictEqual(Core.canClose(ob),false);
ob.evidence.push({type:'outcome_record'});ob.outcome='Next action documented';
assert.strictEqual(Core.canClose(ob),true);
let state=base();
assert.strictEqual(Core.calculatePracticeState(state).level,'green');
state.checks=[{required:true,items:[{required:true,done:false}]}];
assert.strictEqual(Core.calculatePracticeState(state).level,'amber');
state=base();state.obligations=[{title:'Critical unowned',level:'red',status:'open',ownerId:null,dueAt:new Date(Date.now()+60000).toISOString(),stage:'assigned'}];
assert.strictEqual(Core.calculatePracticeState(state).level,'red');
state=base();state.incidents=[{title:'Emergency',level:'black',status:'open'}];
assert.strictEqual(Core.calculatePracticeState(state).level,'black');
state=base();state.whsHazards=[{hazard:'Aggression',residualLevel:'red',status:'open'}];
assert.strictEqual(Core.calculatePracticeState(state).level,'red');
state=base();state.insurance=[{type:'Required insurance',required:true,status:'review_due'}];
assert.strictEqual(Core.calculatePracticeState(state).level,'amber');
const client={id:'c1',nextAppointment:null,coverageStatus:'accepted',safetyPlanReviewDue:false,consentStatus:'current'};
assert.strictEqual(Core.continuityState(client,[]),'amber');
client.nextAppointment=new Date().toISOString();client.coverageStatus='unaccepted';
assert.strictEqual(Core.continuityState(client,[]),'red');
assert.strictEqual(Core.allowedForRole('manageReception','reception'),true);
assert.strictEqual(Core.allowedForRole('manageSupervision','clinical_lead'),true);
assert.strictEqual(Core.allowedForRole('manageSupervision','provisional'),false);
assert.strictEqual(Core.allowedForRole('participateSupervision','provisional'),true);
assert.strictEqual(Core.allowedForRole('manageCompliance','director'),true);
assert.strictEqual(Core.allowedForRole('manageGovernance','psychologist'),false);
console.log('PASS: core state, continuity, evidence-gate, supervision, compliance and role tests');
